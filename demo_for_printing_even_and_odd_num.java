package looping;

public class demo_for_printing_even_and_odd_num {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int i;
		i=2;
		while(i<=100){
			System.out.println(i);
			i=i+2;

	}
		i=1;
		while(i<=100){
			System.out.println(i);
			i=i+2;
	}

}
}
